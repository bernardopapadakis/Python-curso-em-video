# Números são usados primordialmente usados em cálculos, portanto, não usam aspas.
print(7+4)# = 11

# Se você usa números com ', e você somar os valores, ele ao invés de somar ele irá concatenar.
print('7'+'4')# = '74'

# para concatenar dois valores nós podemos ultilizar tanto + como ,
print('São','Paulo')# = 'São Paulo'
