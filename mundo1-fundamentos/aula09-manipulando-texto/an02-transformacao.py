frase = " Curso em Vídeo Python"
print(frase)
print(frase.replace('Python','JavaScript'))# troca a primeira str pela segunda
frase = frase.replace('Python','Android')# troca a primeira str pela segunda e armazena na variável
print(frase.upper())# deixa todas as letras em maiúscula
print(frase.lower())# deixa todas as letras em minúsula
print(frase.capitalize())# deixa todas as letras em minúscula, deixando somente a primeira letra em maiúcula
print(frase.title())# deixa todas as letras em minúscula, deixando somente a primeira letra de cada palavra em maiúscula
print(frase.strip())# apaga todos bytes vazios antes e depois do texto
print(frase.rstrip())# apaga todos bytes vazios no final da str
print(frase.lstrip())# apaga todos bytes vazios no começo da str
